public class Coding34 {
    // 使用MVC模式设计的web应用程序具有以下优点,除了( )?
    // A.可维护行强
    // B.可拓展性强
    // C.代码重复较少
    // D.大大减少代码量

    // 答案D
    // 解析:
    // MVC只是将分管不同功能的逻辑代码进行了隔离，增强了可维护和可扩展性，增强代码复用性，因此可以减少代码重复。
    // 但是不保证减少代码量，多层次的调用模式还有可能增加代码量
    // 而且，一般来说，使用设计模式都会增加代码量
}
